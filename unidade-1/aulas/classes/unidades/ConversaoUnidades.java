public class ConversaoUnidades {
    public static float polegadasParaCentimetros(float polegadas) {
        return polegadas * 2.54f;
    }

    public static float pesParaCentimetros(float pes) {
        return pes * 30.48f;
    }

    public static float milhasParaQuilometros(float milhas) {
        return milhas * 1.609f;
    }
}